EVT_THEORY = {
    "title": "极值理论 (EVT) - 尾部风险量化",
    "intuition": """
**为什么正态分布会失效？**

传统风险模型假设收益率服从正态分布，但金融市场存在**厚尾现象**:
- 极端损失发生概率被严重低估
- 2008金融危机、2020疫情冲击、2022债市调整都是例证
- 用正态分布估计风险，相当于"在暴风雨中打伞"

**极值理论的优势**:
- 专注于分布尾部，而非整体拟合
- 基于广义帕累托分布(GPD)建模超越阈值的部分
- 提供更保守、更可靠的风险估计
- Basel III 已要求银行采用 EVT 方法进行风险计量
""",
    "method": """
**POT方法 (Peaks Over Threshold)**:

1. 选择阈值 u（通常取95%-99%分位数）
2. 对超越阈值的部分拟合GPD:
   F_u(y) = 1 - (1 + ξy/σ)^(-1/ξ)

**参数含义**:

| 参数 | 名称 | 解释 |
|------|------|------|
| ξ (xi) | 形状参数 | 决定尾部厚度 |
| σ (sigma) | 尺度参数 | 尾部衰减速度 |

**形状参数 ξ 的判断**:
- ξ > 0: **厚尾分布**（金融数据典型）——极端损失概率高于指数衰减
- ξ = 0: 指数尾部（如正态分布）
- ξ < 0: 薄尾分布（极端损失概率很低）

**P0修复 - VaR公式**:
VaR = u + (σ/ξ) × [(n/N_u × (1-α))^(-ξ) - 1]
其中 n/N_u 是样本比例因子，正确估计超越阈值的概率。
""",
    "metrics": """
**风险度量指标对比**:

| 指标 | 定义 | 优势 | 劣势 |
|------|------|------|------|
| VaR | 给定置信度下的最大损失 | 简单直观，易于沟通 | 不满足次可加性，不适用于组合优化 |
| ES/CVaR | 超越VaR的平均损失 | 更保守，满足次可加性 | 计算复杂，历史数据要求高 |

**监管趋势**:
- Basel III 已采用 ES (预期损失) 替代 VaR
- ES 更好地捕捉尾部风险，避免"VaR套利"
- 中国银保监会也已要求大型银行采用 ES 方法
"""
}

