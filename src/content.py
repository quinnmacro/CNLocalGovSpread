def get_spread_position_comment(current_spread, spread_mean, spread_std):
    """生成利差位置解读"""
    z_score = (current_spread - spread_mean) / spread_std if spread_std > 0 else 0

    if z_score > 2:
        return f"当前利差位于历史均值上方 **{z_score:.1f}σ**，处于历史高位区间，需警惕均值回归风险。"
    elif z_score > 1:
        return f"当前利差位于历史均值上方 **{z_score:.1f}σ**，处于偏高区间。"
    elif z_score > -1:
        return f"当前利差位于历史均值附近 (**{z_score:.1f}σ**)，处于正常区间。"
    elif z_score > -2:
        return f"当前利差位于历史均值下方 **{abs(z_score):.1f}σ**，处于偏低区间。"
    else:
        return f"当前利差位于历史均值下方 **{abs(z_score):.1f}σ**，处于历史低位区间，需关注反弹可能。"


def get_volatility_comment(current_vol, mean_vol):
    """生成波动率解读"""
    ratio = current_vol / mean_vol if mean_vol > 0 else 1

    if ratio > 1.5:
        return f"当前波动率是历史均值的 **{ratio:.1f}倍**，市场处于高波动状态，建议控制风险敞口。"
    elif ratio > 1.2:
        return f"当前波动率是历史均值的 **{ratio:.1f}倍**，波动率偏高，需关注风险。"
    elif ratio > 0.8:
        return f"当前波动率接近历史均值 (**{ratio:.1f}倍**)，市场状态正常。"
    else:
        return f"当前波动率是历史均值的 **{ratio:.1f}倍**，市场处于低波动状态，可能存在方向性交易机会。"


def get_var_comment(var, es):
    """生成VaR/ES解读"""
    # VaR和ES通常是负数（代表损失），取绝对值计算比率
    var_abs = abs(var) if var else 0.001
    es_abs = abs(es) if es else 0.001
    ratio = es_abs / var_abs if var_abs > 0 else 1

    if ratio > 1.5:
        return f"ES/VaR比率 = **{ratio:.2f}**，尾部风险较高，极端损失可能远超VaR预测。"
    elif ratio > 1.2:
        return f"ES/VaR比率 = **{ratio:.2f}**，存在一定厚尾特征。"
    else:
        return f"ES/VaR比率 = **{ratio:.2f}**，尾部风险相对可控。"
